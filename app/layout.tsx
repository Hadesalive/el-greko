import type React from "react"
import type { Metadata } from "next"
import { Inter, Playfair_Display } from "next/font/google"
import "./globals.css"
import { ThemeProvider } from "@/components/theme-provider"

const inter = Inter({
  subsets: ["latin"],
  variable: "--font-inter",
})

const playfair = Playfair_Display({
  subsets: ["latin"],
  variable: "--font-playfair",
})

export const metadata: Metadata = {
  title: "El Greko - Authentic Greek Restaurant in Freetown",
  description:
    "Experience authentic Greek flavors in the heart of Freetown. Fresh ingredients, traditional recipes, and warm hospitality.",
    generator: 'v0.dev'
}

export default function RootLayout({
  children,
}: {
  children: React.ReactNode
}) {
  return (
    <html lang="en" suppressHydrationWarning>
      <body className={`${inter.variable} ${playfair.variable} font-sans antialiased`}>
        <ThemeProvider defaultTheme="dark" storageKey="elgreko-theme">
          {children}
        </ThemeProvider>
      </body>
    </html>
  )
}
